	function WishList(ele) {
		var session_id = <?php echo intval($_SESSION['id']); ?>;

		if (session_id) {

			jQuery.ajax({
				url: "add-to-wishlist.php",
				data: { session_id: session_id, product_id: ele },
				type: "POST",
				success: function (data) {
					$("#ack").html(data);
				},
				error: function () { }
			});
		} else {
			document.location = 'login.php';
		}
	}

